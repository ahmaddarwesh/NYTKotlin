package com.challenge.nyt.model.article;

public class MediaMetadataItem{
	private String format;
	private int width;
	private String url;
	private int height;

	public String getFormat(){
		return format;
	}

	public int getWidth(){
		return width;
	}

	public String getUrl(){
		return url;
	}

	public int getHeight(){
		return height;
	}
}
